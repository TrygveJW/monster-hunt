/// <summary>
/// The constants for the different game states
/// </summary>
public enum GAME_STATE {
    /// <summary>
    /// The Game should exit
    /// </summary>
    EXIT,

    /// <summary>
    /// Go to main menu
    /// </summary>
    MAIN_MENU,

    /// <summary>
    /// Go to the test level
    /// </summary>
    TEST_LEVEL,

    /// <summary>
    /// Go to the letter level
    /// </summary>
    LETTER_LEVEL,

    /// <summary>
    /// Go to the next level
    /// </summary>
    NEXT_LEVEL,

    /// <summary>
    /// Start the game
    /// </summary>
    START_GAME,
    /// <summary>
    /// Go to scoreboard
    /// </summary>
    SCOREBOARD,
}